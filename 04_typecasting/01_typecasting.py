# typecasting is the process of converting one data type to another data type.
# in python, we can use the built-in functions int(), float(), str(), bool() to convert data types. 

user_input = input("Enter a number: ")  # This will be a string
print("You entered:", user_input)

# Converting the string input to an integer
user_input_int = int(user_input)
print("You entered (as integer):", user_input_int)