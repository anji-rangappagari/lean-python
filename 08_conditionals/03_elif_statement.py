# elif statement is a conditional statement that allows us to execute a block of code if a certain condition is true, and if the condition is false, it will check for another condition. The syntax of an elif statement is as follows:    

age = 18

if age > 18:
    print("You are an adult and you are eligible to vote.")

elif age == 18:
    print("Lets have interview for review.")

else:
    print("You are not an adult and you are not eligible to vote.")

print("Thank you for using our voting system.")