# tuples    
# tuples are immutable sequences, typically used to store collections of heterogeneous data (such as the 3D coordinates of a point) and are often used for data that should not be changed throughout the program.
# tuples are defined by enclosing the elements in parentheses () and separating them with commas.
# creating a tuple
my_tuple = (11, 25, 34)
# accessing elements of a tuple
print(my_tuple)  # Output: (11, 25, 34)
print(my_tuple[0])  # Output: 11
print(my_tuple[1])  # Output: 25
print(my_tuple[2])  # Output: 34

# how to create a tuple with a single element
single_element_tuple = (42,)  # Note the comma after the element
print(single_element_tuple)  # Output: (42,)