# Using format(), create a sentence:
# "My name is Anji and I am 25 years old."
# by passing "Anji" and 25 as variables.

# Do the same using f-strings.

name = "Anji"
age = 25

print(f"My name is {name} and I am {age} years old.")
print("My name is {} and I am {} years old.".format(name, age)) # we can do this as well but f-strings are more readable and easier to use.


