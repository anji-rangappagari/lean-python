# 1. Create a Simple Class and Object
# Create a class Car with a method drive() that prints "Car is moving".
# Create an object of Car and call drive().

class Car:  # class name should be in capital letter
    def drive(self):  # method name should be in small letter
        print("Car is moving") # method body should be indented
my_car = Car() # create an object of Car class
my_car.drive()  # Output: Car is moving



