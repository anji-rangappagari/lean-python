# while loop is a control flow statement that allows us to execute a block of code repeatedly as long as a certain condition is true. The syntax of a while loop is as follows:

# i = 1
# while i <= 10:
#     print("5 *", i, "=", 5 * i)
#     i += 1  # This is equivalent to i = i + 1, it increments the value of i by 1 in each iteration of the loop.

i = 1
while i < 6:
    print(i)
    i = i + 1