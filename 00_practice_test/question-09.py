# Mark True or False:
# Python code must always end with a semicolon ; .
# The # symbol is used for comments in Python.
# "123" and 123 are the same in Python.
# The * operator is used for multiplication.
# \n creates a new line.
# Variables in Python can start with numbers.
# int("10") + 5 gives 15 .
is_semicolon_required = False  # False, Python does not require semicolons
is_comment_symbol = True  # True, # is used for comments in Python
is_string_and_int_same = False  # False, "123" is a string and 123 is an integer
is_multiplication_operator = True  # True, * is used for multiplication
is_newline_escape = True  # True, \n creates a new line
is_variable_start_with_number = False  # False, variables cannot start with numbers
is_int_conversion_correct = True  # True, int("10") + 5 gives 15
print(is_semicolon_required)
print(is_comment_symbol)
print(is_string_and_int_same)
print(is_multiplication_operator)
print(is_newline_escape)
print(is_variable_start_with_number)
print(is_int_conversion_correct)
