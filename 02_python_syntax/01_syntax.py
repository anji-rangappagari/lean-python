#  python syntax
#  python syntax is the set of rules that defines how a python program is written and interpreted   
#  python syntax is easy to learn and understand, it is designed to be readable and simple.
#  python syntax is based on indentation, which means that the code blocks are defined by their indentation level.
#  python uses whitespace to define the scope of loops, functions, and classes.
#  python syntax is case-sensitive, which means that variables, functions, and classes must be defined with the correct case.
#  python syntax is also flexible, which means that you can write code in different ways to achieve the same result.
#  python syntax is also dynamic, which means that you can change the type of a variable at runtime.
#  python syntax is also interpreted, which means that the code is executed line by line, which allows for interactive programming and debugging.

